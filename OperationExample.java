package demoClass;

public class OperationExample {

	
	    public static void main(String[] args) {
	    	
	        
	        int resultA = -5 + 8 * 6;
	        System.out.println("Result of -5 + 8 * 6 is: " + resultA);       	//  For Operation a: -5 + 8 * 6

	       
	        
	        int resultB = (55 + 9) % 9;
	        System.out.println("Result of (55 + 9) % 9 is: " + resultB);       // For Operation b: (55+9) % 9


	        
	        int resultC = 20 + -3 * 5 / 8;                        		    	//For Operation c: 20 + -3*5 / 8
	        System.out.println("Result of 20 + -3 * 5 / 8 is: " + resultC);

	        
	        																	//For  Operation d: 5 + 15 / 3 * 2 - 8 % 3
	        int resultD = 5 + 15 / 3 * 2 - 8 % 3;
	        System.out.println("Result of 5 + 15 / 3 * 2 - 8 % 3 is: " + resultD);
	    }
	}

